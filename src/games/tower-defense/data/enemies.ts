let _enemyIdCounter = 0;

export function createEnemyInstance(
  defId: EnemyTypeId,
  startX: number,
  startY: number,
  modifiers?: EnemyModifier
): EnemyInstance {
  const def = ENEMY_DEFS[defId];
  const hpMult = modifiers?.hpMult ?? 1;
  const maxHp = Math.round(def.hp * hpMult);
  return {
    id: `enemy_${++_enemyIdCounter}`,
    defId,
    hp: maxHp,
    maxHp,
    x: startX,
    y: startY,
    pathIndex: 1,
    distanceTraveled: 0,
    statusEffects: [],
    slowFactor: 1.0,
    isAlive: true,
  };
}

export function getEnemyDef(id: EnemyTypeId): EnemyDef {
  return ENEMY_DEFS[id];
}
